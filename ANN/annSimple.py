def تدريب_الإدراك(البيانات, الأوزان, التحيز, معدل_التعلم, عدد_الدورات=10):
    """
    دالة لتدريب نموذج الإدراك ذو الطبقة الواحدة.

    :مُعطى البيانات: قائمة من ثلاثيات (المدخل_1, المدخل_2, الناتج_المطلوب) تمثل بيانات التدريب.
    :مُعطى الأوزان: قائمة [الوزن_1, الوزن_2] تمثل الأوزان الابتدائية.
    :مُعطى التحيز: قيمة التحيز (ب) الابتدائية.
    :مُعطى معدل_التعلم: قيمة معدل التعلم (ن).
    :مُعطى عدد_الدورات: عدد الدورات لتكرار التدريب (افتراضي = 10).
    """
    الوزن_1, الوزن_2 = الأوزان  # تفكيك الأوزان الابتدائية
    ب = التحيز  # التحيز الابتدائي

    print("### بدء عملية التدريب ###")
    print(f"الأوزان الابتدائية: الوزن_1={الوزن_1}, الوزن_2={الوزن_2}, التحيز={ب}, معدل التعلم={معدل_التعلم}")
    
    for دورة in range(عدد_الدورات):
        print(f"\n--- الدورة {دورة + 1} ---")
        for المدخل_1, المدخل_2, الناتج_المطلوب in البيانات:
            # حساب المجموع الوزني (التنشيط)
            المجموع = الوزن_1 * المدخل_1 + الوزن_2 * المدخل_2 + ب
            الناتج_المتوقع = 1 if المجموع >= 0 else 0  # تطبيق دالة التنشيط (عتبة)

            # تحديث الأوزان إذا كان الناتج_المطلوب ≠ الناتج_المتوقع
            if الناتج_المطلوب != الناتج_المتوقع:
                print(f"قبل التحديث: الوزن_1={الوزن_1}, الوزن_2={الوزن_2}, التحيز={ب}, المجموع={المجموع}, الناتج_المتوقع={الناتج_المتوقع}, الهدف={الناتج_المطلوب}")
                الوزن_1 += معدل_التعلم * (الناتج_المطلوب - الناتج_المتوقع) * المدخل_1
                الوزن_2 += معدل_التعلم * (الناتج_المطلوب - الناتج_المتوقع) * المدخل_2
                ب += معدل_التعلم * (الناتج_المطلوب - الناتج_المتوقع)
                print(f"بعد التحديث: الوزن_1={الوزن_1}, الوزن_2={الوزن_2}, التحيز={ب}")
            else:
                print(f"لا تحديث: الوزن_1={الوزن_1}, الوزن_2={الوزن_2}, التحيز={ب}, المجموع={المجموع}, الناتج_المتوقع={الناتج_المتوقع}, الهدف={الناتج_المطلوب}")
    
    print("\n### نهاية التدريب ###")
    print(f"الأوزان النهائية: الوزن_1={الوزن_1}, الوزن_2={الوزن_2}, التحيز={ب}")
    return [الوزن_1, الوزن_2], ب


